package com.db.webservice.security;

public class ValueConvertException extends RuntimeException
{
    private static final long serialVersionUID = -2383783117905504434L;
    
    public ValueConvertException() {
    }
    
    public ValueConvertException(final String message) {
        super(message);
    }
    
    public ValueConvertException(final Throwable cause) {
        super(cause);
    }
}